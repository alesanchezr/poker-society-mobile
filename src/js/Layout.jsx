import React from 'react';
import Flux from "@4geeksacademy/react-flux-dash";
import { BrowserRouter, Route, Switch } from "react-router-dom";
import Home from "./views/Home.jsx";
import Calendar from "./views/Calendar.jsx";
import Tournament from "./views/Tournament.jsx";
import Casino from "./views/Casino.jsx";

export default class Layout extends Flux.View {
    render() {
        return (
            <div>
                <BrowserRouter>
                    <div>
                        <Switch>
                            <Route exact path="/index.html" component={Home} />
                            <Route exact path="/" component={Home} />
                            <Route exact path="/home" component={Home} />
                            <Route exact path="/calendar/:cal_id" component={Calendar} />
                            <Route exact path="/tournament/:tour_id" component={Tournament} />
                            <Route exact path="/casino/:cas_id" component={Casino} />
                            <Route render={() => <h1>Not found!</h1>} />
                        </Switch>
                    </div>
                </BrowserRouter>
            </div>
        );
    }
}
